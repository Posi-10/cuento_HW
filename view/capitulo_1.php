            <p><span class="posi">-M</span>ateo, vení. Te voy a enseñar mi videojuego preferido de cuando era chico –me dijo mi papá-. Se llama Pacman. ¿Lo jugaste alguna vez?</p>
            <p>Negué con la cabeza.</p>
            <p>Me encanta jugar con papá. Antes solía volver de trabajar muy tarde y casi no lo veía en toda la semana. Pero desde hacía unos meses me dedicaba mucho tiempo.</p>
            <p>Papá conectó los cables de un viejo aparato a la televisión y colocó el cartucho. En la pantalla se dibujó un laberinto con fantasmas y un círculo con boca que parecía tener muchas ganas de comer. Mi papá comenzó la partida. ¡Era muy divertido! El juego es simple, se trata de comer puntos y frutas en un laberinto, a la vez que se trata de evitar que los fantasmas te atrapen.</p>
            <p>Nos pasamos la tarde completa jugando sin parar. Mamá nos preparó un bizcochuelo de chocolate con dulce de leche y hasta se terminó sumando a la competencia. ¡Qué bien la pasamos!</p>
            <p>Se me hizo costumbre llegar de la escuela y jugar dos o tres partidas por día. Ya le había tomado práctica y pasaba varios niveles sin perder vidas.</p>
            <p>Un día, estaba jugando cuando escuché que papá y mamá estaban hablando en la habitación un poco fuerte. Puse pausa en el juego y me acerqué.</p>
            <p>Pude oír que mamá estaba llorando. Papá le decía que no se preocupe, que él no se iba a rendir nunca y que estaba seguro que sus próximos análisis iban a mostrar mejoría. ¡Qué tristeza sentí! Mi papá estaba enfermo.</p>
